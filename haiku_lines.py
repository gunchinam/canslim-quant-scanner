# 우선순위 3: 별점만 있을 때 폴백
BY_STARS = {
    5: ["★★★★★ — 4축 모두 컨펌, 망설일 이유가 없다"],
    4: ["★★★★ — 강한 신호, 분할 진입 고려"],
    3: ["★★★ — 반쪽 신호, 추가 컨펌 기다리자"],
    2: ["★★ — 신호가 약하다, 관망이 낫다"],
    1: ["★ — 위험 우위 자리, 손대지 말자"],
}


def pick_haiku(phase: str,
               stars: int,
               *,
               bull_div: bool = False,
               bear_div: bool = False,
               squeeze:  bool = False,
               upper_break: bool = False,
               v_confirm:   bool = False,
               seed: int | None = None) -> str:
    """phase, stars, 플래그를 보고 가장 어울리는 한 줄을 결정적으로 선택."""
    rnd = random.Random(seed if seed is not None else hash((phase, stars,
        bull_div, bear_div, squeeze, upper_break, v_confirm)))

    # 1순위: 특수 시그널 우선
    for flag, key in [
        (bull_div,    "bull_div"),
        (bear_div,    "bear_div"),
        (squeeze,     "squeeze"),
        (upper_break, "upper_break"),
        (v_confirm,   "v_confirm"),
    ]:
        if flag and SPECIAL.get(key):
            return rnd.choice(SPECIAL[key])

    # 2순위: 국면별
    pool = BY_PHASE.get(phase)
    if pool:
        return rnd.choice(pool)

    # 3순위: 별점
    return BY_STARS.get(stars, ["—"])[0]


if __name__ == "__main__":
    # 간단한 자가검증
    cases = [
        ("강한 상승", 5, dict(upper_break=True, v_confirm=True)),
        ("브레이크아웃 임박", 4, dict(squeeze=True)),
        ("추세 전환 경고", 2, dict(bear_div=True)),
        ("조정 중", 3, dict(bull_div=True)),
        ("횡보", 3, {}),
    ]
    for ph, st, fl in cases:
        print(f"[{ph}/{st}★] {pick_haiku(ph, st, **fl)}")
