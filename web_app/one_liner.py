def _strip_period(text: str) -> str:
    return text.rstrip(" .。")


def _jgallize(text: str) -> str:
    repl = (
        ("입니다", "임"),
        ("습니다", "음"),
        ("합니다", "함"),
        ("보입니다", "보임"),
        ("있습니다", "있음"),
        ("됩니다", "됨"),
        ("좋습니다", "좋음"),
        ("나쁩니다", "나쁨"),
        ("가깝습니다", "가까움"),
        ("필요합니다", "필요함"),
        ("가능합니다", "가능함"),
    )
    out = _strip_period(text)
    for a, b in repl:
        out = out.replace(a, b)
    return out


