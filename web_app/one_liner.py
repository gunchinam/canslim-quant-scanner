def _num(v, default=0.0):
    try:
        if v is None:
            return default
        return float(v)
    except (TypeError, ValueError):
        return default


def _bucket(d: dict) -> str:
    per     = _num(d.get("_PER"))
    roe     = _num(d.get("_ROE"))
    eps_g   = _num(d.get("_EPSGrowth"))
    rsi     = _num(d.get("RSI"), 50)
    mom12   = _num(d.get("Mom12M"))
    mom3    = _num(d.get("_Mom3M"))
    near_h  = bool(d.get("NearHighPass"))
    entry   = (d.get("EntryStatus") or "").upper()
    score   = _num(d.get("TotalScore"))
    signal  = (d.get("Signal") or "").upper()
    dd      = _num(d.get("Drawdown"))
    op_mgn  = _num(d.get("_OperatingMargin"))
    eps_acc = bool(d.get("EPSAcceleration"))
    leader  = bool(d.get("IsLeader"))

    # Drawdown은 ratio(-0.3) 또는 percent(-30) 둘 다 대응
    dd_pct = dd * 100 if -1 <= dd <= 0 else dd
    # operatingMargins는 yfinance가 비율(0.25)로 반환
    op_pct = op_mgn * 100 if 0 <= op_mgn <= 1 else op_mgn

    # ── 우선순위 순서 (위에서 아래로 first-match) ──
    # 위험/회피 신호 먼저
    if "AVOID" in signal or (score and score < 30):
        return "AVOID"
    if dd_pct <= -25 and mom3 <= -8:
        return "FALLING_KNIFE"

    # 위치성(RSI 극단) 먼저 — 트렌드 종목에서도 빠지면 안 됨
    if rsi >= 75:
        return "OVERBOUGHT"
    if rsi <= 30 and roe >= 5:
        return "OVERSOLD"

    # 강한 매수 (점수 기반)
    if score >= 80:
        return "STRONG_BUY"

    # 펀더+가격 결합 — 더 구체적 버킷부터
    if eps_acc and mom3 >= 3:
        return "EARNINGS_BEAT"
    if leader and score >= 60:
        return "SECTOR_LEADER"
    if mom12 >= 50 and (per <= 0 or per >= 80):
        return "STORY_STOCK"
    if per >= 40 and (mom3 < 0 or eps_g < 10):
        return "BUBBLE"
    if per >= 30 and eps_g >= 20 and mom12 >= 20:
        return "EXPENSIVE_JUSTIFIED"
    if 0 < per <= 15 and roe >= 15 and eps_g >= 8:
        return "TRUE_VALUE"
    if 0 < per <= 15 and (roe < 8 or eps_g < 0):
        return "VALUE_TRAP"
    if op_pct >= 25 and roe >= 18 and eps_g < 15:
        return "CASH_COW"

    # 추세성 — 위 모든 펀더 버킷 이후
    if entry in ("STRONG", "GREEN") and near_h and mom12 >= 30:
        return "BREAKOUT"
    if near_h and 55 <= rsi < 72 and mom12 >= 20:
        return "MOMENTUM_LEADER"
    if entry in ("STRONG", "GREEN") and near_h:
        return "BREAKOUT"

    # 횡보·방어
    if roe >= 18 and abs(mom12) < 8:
        return "SLEEPING_GIANT"
    if 0 < per <= 25 and roe >= 10 and abs(mom12) < 15:
        return "DEFENSIVE"

    # 폴백을 다양화: 점수·RSI·모멘텀 조합으로 분기
    if score >= 60 and mom12 >= 10:
        return "MOMENTUM_LEADER"
    if score >= 60:
        return "SECTOR_LEADER"
    if rsi >= 60 and mom3 >= 3:
        return "MOMENTUM_LEADER"
    if 0 < per <= 20 and roe >= 8:
        return "DEFENSIVE"
    if per >= 25 and eps_g >= 10:
        return "STORY_STOCK"
    return "NEUTRAL"


def get_one_liner(d: dict) -> str:
    """종목 dict에서 한줄평을 결정해 반환한다."""
    bucket = _bucket(d)
    phrases = _PHRASES.get(bucket) or _PHRASES["NEUTRAL"]
    ticker = (d.get("Ticker") or "").upper()
    # 시드에 점수/모멘텀/RSI 양자화값을 섞어 같은 버킷이라도 종목·조건별로 분산
    s_q  = int(_num(d.get("TotalScore")) // 5)
    m_q  = int(_num(d.get("Mom12M")) // 5)
    r_q  = int(_num(d.get("RSI"), 50) // 5)
    seed = hashlib.md5(f"{ticker}|{bucket}|{s_q}|{m_q}|{r_q}".encode("utf-8")).hexdigest()
    idx = int(seed, 16) % len(phrases)
    return phrases[idx]


