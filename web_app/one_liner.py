def _strip_period(text: str) -> str:
    return text.rstrip(" .。")


def _jgallize(text: str) -> str:
    repl = (
        ("입니다", "임"),
        ("습니다", "음"),
        ("합니다", "함"),
        ("보입니다", "보임"),
        ("있습니다", "있음"),
        ("됩니다", "됨"),
        ("좋습니다", "좋음"),
        ("나쁩니다", "나쁨"),
        ("가깝습니다", "가까움"),
        ("필요합니다", "필요함"),
        ("가능합니다", "가능함"),
    )
    out = _strip_period(text)
    for a, b in repl:
        out = out.replace(a, b)
    return out


def _data_tag(d: dict, bucket: str) -> str:
    """버킷별 핵심 수치를 짧은 태그로 반환."""
    per = _num(d.get("_PER"))
    roe = _num(d.get("_ROE"))
    eps_g = _num(d.get("_EPSGrowth"))
    rsi = _num(d.get("RSI"), 50)
    mom12 = _num(d.get("Mom12M"))
    mom3 = _num(d.get("_Mom3M"))
    dd = _num(d.get("Drawdown"))
    op_mgn = _num(d.get("_OperatingMargin"))
    score = _num(d.get("TotalScore"))

    dd_pct = dd * 100 if -1 <= dd <= 0 else dd
    op_pct = op_mgn * 100 if 0 <= op_mgn <= 1 else op_mgn
    # Mom12M·_ROE·_EPSGrowth는 quant_nexus에서 비율로 전달 (0.18=18%) → 퍼센트
    roe   = roe * 100
    mom12 = mom12 * 100
    eps_g = eps_g * 100

    p = []
    if bucket == "TRUE_VALUE":
        if per > 0: p.append(f"주가수익비율 {per:.0f}배")
        if roe: p.append(f"자본수익률 {roe:.0f}%")
        if eps_g: p.append(f"이익성장 {eps_g:+.0f}%")
    elif bucket == "VALUE_TRAP":
        if per > 0: p.append(f"주가수익비율 {per:.0f}배")
        if roe: p.append(f"자본수익률 {roe:.0f}%")
    elif bucket == "EARNINGS_BEAT":
        if eps_g: p.append(f"이익성장 {eps_g:+.0f}%")
        if op_pct > 0: p.append(f"영업이익률 {op_pct:.0f}%")
    elif bucket in ("BUBBLE", "EXPENSIVE_JUSTIFIED"):
        if per > 0: p.append(f"주가수익비율 {per:.0f}배")
        if eps_g: p.append(f"이익성장 {eps_g:+.0f}%")
    elif bucket == "STORY_STOCK":
        if per > 0: p.append(f"주가수익비율 {per:.0f}배")
        elif per < 0: p.append("적자기업")
        if mom12: p.append(f"1년수익률 {mom12:+.0f}%")
    elif bucket == "OVERBOUGHT":
        p.append(f"과열지수 {rsi:.0f}")
    elif bucket == "OVERSOLD":
        p.append(f"과매도지수 {rsi:.0f}")
        if roe: p.append(f"자본수익률 {roe:.0f}%")
    elif bucket in ("MOMENTUM_LEADER", "BREAKOUT"):
        if mom12: p.append(f"1년수익률 {mom12:+.0f}%")
        if rsi: p.append(f"모멘텀지수 {rsi:.0f}")
    elif bucket == "FALLING_KNIFE":
        if dd_pct: p.append(f"고점대비 {dd_pct:.0f}%")
        if mom3: p.append(f"3개월 {mom3:+.0f}%")
    elif bucket == "SLEEPING_GIANT":
        if roe: p.append(f"자본수익률 {roe:.0f}%")
    elif bucket == "CASH_COW":
        if op_pct > 0: p.append(f"영업이익률 {op_pct:.0f}%")
        if roe: p.append(f"자본수익률 {roe:.0f}%")
    elif bucket == "DEFENSIVE":
        if per > 0: p.append(f"주가수익비율 {per:.0f}배")
        if roe: p.append(f"자본수익률 {roe:.0f}%")
    elif bucket == "SECTOR_LEADER":
        pass
    elif bucket == "STRONG_BUY":
        pass
    elif bucket == "AVOID":
        pass
    # NEUTRAL: 태그 없음

    # ── 센티먼트/수급 신호는 별도 풀(extra)에 모아 '강한 것 우선'으로 추림 ─
    #   한줄평 밑 설명이 구구절절해지지 않도록, 핵심 지표(p) + 신호 소수만 노출.
    extra = []

    # Finnhub 센티먼트 태그 (US 종목)
    if d.get("_FH_Available"):
        earn_surp = _num(d.get("_FH_EarnSurprise"))
        earn_streak = int(d.get("_FH_EarnStreak") or 0)
        rec_change = d.get("_FH_RecChange", "")
        insider_net = int(d.get("_FH_InsiderNet") or 0)
        if abs(earn_surp) >= 1:
            extra.append(f"실적서프라이즈 {earn_surp:+.1f}%")
        if earn_streak >= 3:
            extra.append(f"실적연속상회 {earn_streak}분기")
        if rec_change == "upgrade":
            extra.append("애널리스트 상향")
        elif rec_change == "downgrade":
            extra.append("애널리스트 하향")
        if insider_net > 0:
            extra.append("내부자 매수")
        elif insider_net < -10000:
            extra.append("내부자 매도")

    # yfinance 센티먼트 태그 (US 종목)
    if d.get("_YF_Available"):
        short_pct = _num(d.get("_YF_ShortPctFloat"))
        inst_pct = _num(d.get("_YF_InstPct"))
        rec_key = d.get("_YF_RecKey", "")
        n_analysts = int(_num(d.get("_YF_NumAnalysts")))
        target_gap = _num(d.get("_YF_TargetGapPct"))
        if short_pct >= 2:
            extra.append(f"공매도 {short_pct:.1f}%")
        if inst_pct > 0:
            extra.append(f"기관보유 {inst_pct:.0f}%")
        if rec_key and n_analysts:
            _rec_kr = {"strong_buy": "적극매수", "buy": "매수", "hold": "보유", "sell": "매도", "strong_sell": "적극매도"}
            extra.append(f"컨센서스 {_rec_kr.get(rec_key, rec_key)}({n_analysts}명)")
        if abs(target_gap) >= 5:
            extra.append(f"목표가괴리 {target_gap:+.0f}%")

    # KIS 수급 태그 (KR 종목만)
    if d.get("_KIS_Available"):
        frgn = int(d.get("_KIS_Foreign") or 0)
        inst = int(d.get("_KIS_Institution") or 0)
        def _fmt_qty(v: int) -> str:
            if abs(v) >= 10000:
                return f"{v / 10000:+.1f}만주"
            return f"{v:+,}주"
        if abs(frgn) >= 1000:
            extra.append(f"외국인 {_fmt_qty(frgn)}")
        if abs(inst) >= 1000:
            extra.append(f"기관 {_fmt_qty(inst)}")

    # 버킷 핵심 지표 우선(정의 수치 보존) → 남는 자리는 신호로 채움, 최대 3개.
    picked = (p + extra)[:3]
    return " · ".join(picked) if picked else ""


def _stylize_one_liner(base: str, d: dict, bucket: str) -> str:
    """주갤 톤 문구 + 실제 수치 태그."""
    base = _strip_period(base)
    tag = _data_tag(d, bucket)
    return f"{base} {tag}" if tag else base

